# Needs Python 3.6
# Usage: python check.py skyrim.belongs.to.the.nords.ns.agency
def main():
    if len(sys.argv) != 2:
        print("Usage: python %s subdomain" % (__file__))
        print("e.g.: python %s something.ns.agency" % (__file__))
        exit(-1)
    subdomain = sys.argv[1]
    check_pwd(subdomain)

seed = "estryuiobase_stringuytrdeswart4iyuhkjgdryt5u6ti7yuhkjvgur67ioyulhkjvgdiyuhkje5yugkjhbvfgcdre45y678"

def loop_hash(payload):
    for i in range(900000):
        payload = hashlib.sha512(payload.encode('utf-8')).hexdigest()
    return payload

def check_pwd(fulldomain):
    full_text = f'{fulldomain}{seed}'
    hashed = loop_hash(full_text)
    if hashed in hashes:
        print("Valid subdomain!")
        exit(0)
    print("Subdomain not found")


if __name__ == "__main__":
    main()

